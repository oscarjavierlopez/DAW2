//Propiedades(son como constantes y no se les invoca con parentesis)
console.log("El valor de PI es: " + Math.PI);
console.log("El valor de E es: " + Math.E);

//Funciones
console.log("El valor absoluto de -1 es: " + Math.abs(-1));
console.log("El seno de 0 es: " + Math.sin(0) + " ,El coseno de 0 es: " + Math.cos(0) + " y su tangente es: " + Math.tan(0) );
console.log("e elevado a 2 es: " + Math.exp(2)); //eleva e a un numero que le pasemos
console.log("El logaritmo de 100 es: "+ Math.log10(100));//tambien está disponible para logaritmo en base e o cualquier base
console.log("El ceil de -5.5 es: " + Math.ceil(-5.5) + " y el de 5.5 es: " + Math.ceil(5.5)); //Si es un numero sin decimal se queda en el numero y si es decimal redondea al superior
console.log("El floor de -5.5 es: " + Math.floor(-5.5) + " y el de 5.5 es: " + Math.floor(5.5)); //Si es un numero sin decimal se queda en el numero y si es decimal redondea al inferior
console.log("5.5 redondeado es: " + Math.round(5.5) + " y 5.4 es: " + Math.round(5.4));
console.log("2 elevado a la 3 es: " + Math.pow(2,3));
console.log("El número más pequeño de 1, 2 y 3 es: " + Math.min(1,2,3));
console.log("El número más grande de 1, 2 y 3 es: " + Math.max(1,2,3));
console.log("La raíz cuadrada de 4 es: " + Math.sqrt(4));
console.log("Un número random ente 1 y 100 es: " + Math.random() * 100 + 1);


